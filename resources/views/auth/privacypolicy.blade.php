            <p class="text-sm text-gray-600 mb-6">
                Your privacy is important to us. This Privacy Policy explains how the Local Government Unit (LGU) and Barangay collects, uses, discloses, and safeguards your information when you use the RESIDENTE digital services platform.
            </p>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">I. Information We Collect</h2>
                <ul class="list-disc list-inside space-y-2">
                    <li><strong>Personal Data:</strong> Full name, address, birth date, civil status, contact number, and email address.</li>
                    <li><strong>Government-Issued IDs:</strong> Scanned copies of valid IDs for verification purposes (e.g., National ID, Driver's License, Passport).</li>
                    <li><strong>Transaction Data:</strong> Records of barangay clearances, certifications, permits, CEDULA payments, and community tax certificates.</li>
                    <li><strong>Usage Data:</strong> Log data, IP addresses, and device information used to access the app for security purposes.</li>
                </ul>
            </section>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">II. How We Use Your Information</h2>
                <ul class="list-disc list-inside space-y-2">
                    <li>To verify residency within the barangay jurisdiction and validate eligibility for services.</li>
                    <li>To process requests for barangay clearances, certificates, permits, and other official documents.</li>
                    <li>To facilitate communication between residents and barangay/LGU officials regarding announcements, programs, and services.</li>
                    <li>To maintain accurate demographic records for community planning and disaster preparedness.</li>
                    <li>To generate reports required by higher government units and comply with statistical reporting obligations.</li>
                </ul>
            </section>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">III. Data Retention and Security</h2>
                <p>
                    We implement industry-standard encryption and secure storage protocols to protect your data. Your information is retained in accordance with the <strong>National Archives of the Philippines guidelines</strong> and relevant government record-keeping requirements. <strong>We do not sell or share your personal data with private third-party companies or advertisers.</strong> Data sharing only occurs with authorized government agencies as mandated by law.
                </p>
            </section>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">IV. Barangay Service Request Protocol</h2>
                <p class="mb-3">
                    When using RESIDENTE to request barangay services, you agree to the following:
                </p>
                <ul class="list-disc list-inside space-y-2">
                    <li><strong>Accurate Information:</strong> All information submitted must be truthful and accurate. Falsification of documents or misrepresentation is punishable under Philippine law.</li>
                    <li><strong>Processing Time:</strong> Standard processing times vary per service type (e.g., Barangay Clearance: 3-5 business days, Indigency Certificate: 1-2 business days). Emergency requests will be prioritized.</li>
                    <li><strong>Payment & Fees:</strong> Applicable fees as prescribed by barangay ordinances must be paid through authorized payment channels. Official receipts will be provided digitally and/or physically.</li>
                    <li><strong>Document Pickup:</strong> Unless otherwise specified, approved documents must be claimed at the Barangay Hall during office hours with valid identification.</li>
                </ul>
            </section>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">V. Community Bulletin & Announcements</h2>
                <p class="mb-3">
                    RESIDENTE provides real-time updates on barangay and LGU activities including:
                </p>
                <ul class="list-disc list-inside space-y-2">
                    <li><strong>Executive Orders & Memorandums:</strong> Official government directives such as class/work suspensions, disaster advisories, and policy changes.</li>
                    <li><strong>Barangay Programs:</strong> Health missions, livelihood programs, senior citizen assistance, and youth activities.</li>
                    <li><strong>Public Safety Alerts:</strong> Crime alerts, health advisories, road closures, and emergency notifications.</li>
                    <li><strong>Community Events:</strong> Fiestas, sports festivals, barangay assemblies, and other gatherings.</li>
                </ul>
                <p class="text-sm text-gray-600 mt-2">
                    <strong>Note:</strong> All announcements published are verified by authorized barangay personnel. For life-threatening emergencies, always contact emergency hotlines (911, local fire/police stations) directly.
                </p>
            </section>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">VI. Blotter & Incident Reporting</h2>
                <ul class="list-disc list-inside space-y-2">
                    <li><strong>Purpose:</strong> The digital blotter module allows residents to report incidents, disputes, and concerns to the Barangay Peacekeeping Action Team (BPAT) or Lupon Tagapamayapa.</li>
                    <li><strong>Confidentiality:</strong> Incident reports are treated with confidentiality and will only be disclosed to parties directly involved or as required by law enforcement.</li>
                    <li><strong>Mediation Process:</strong> RESIDENTE is a tool for documentation only. Actual mediation and settlement proceedings follow the Katarungang Pambarangay Law (P.D. 1508).</li>
                    <li><strong>Limitations:</strong> This is NOT a substitute for police investigation. Crimes requiring formal criminal complaints must still be filed at the PNP station.</li>
                </ul>
            </section>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">VII. Legal Compliance</h2>
                <p class="mb-2">
                    This Privacy Policy fully complies with Philippine laws and regulations, including:
                </p>
                <ul class="list-disc list-inside space-y-2">
                    <li><strong>Data Privacy Act of 2012 (Republic Act No. 10173)</strong> – Protects individual personal information in information and communications systems.</li>
                    <li><strong>Local Government Code of 1991 (Republic Act No. 7160)</strong> – Grants barangays authority to maintain resident records and provide basic services.</li>
                    <li><strong>E-Commerce Act (Republic Act No. 8792)</strong> – Recognizes the validity of electronic documents and signatures.</li>
                    <li><strong>Katarungang Pambarangay Law (P.D. 1508)</strong> – Governs dispute settlement at the barangay level.</li>
                </ul>
                <p class="mt-3">
                    We are committed to protecting your personal information and ensuring transparency in how we collect, use, and store your data in accordance with these laws.
                </p>
            </section>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">VIII. Your Rights Under Data Privacy Law</h2>
                <p class="mb-2">
                    Under the Data Privacy Act of 2012, you have the right to:
                </p>
                <ul class="list-disc list-inside space-y-2">
                    <li><strong>Access:</strong> Request a copy of your personal data stored in the RESIDENTE system.</li>
                    <li><strong>Correction:</strong> Request correction of inaccurate, incomplete, or outdated information.</li>
                    <li><strong>Erasure/Blocking:</strong> Request deletion or blocking of your data when it is no longer necessary or when withdrawal of consent is made (subject to legal retention requirements).</li>
                    <li><strong>Object:</strong> Object to the processing of your data for direct marketing, automated processing, or profiling.</li>
                    <li><strong>Portability:</strong> Request electronic copies of your data in a commonly used format.</li>
                    <li><strong>Lodge Complaints:</strong> File complaints with the National Privacy Commission if you believe your data privacy rights have been violated.</li>
                </ul>
            </section>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">IX. Data Sharing with Government Agencies</h2>
                <p class="mb-2">
                    Your information may be shared with authorized government agencies for the following purposes:
                </p>
                <ul class="list-disc list-inside space-y-2">
                    <li>Reporting to the <strong>Philippine Statistics Authority (PSA)</strong> for national census and demographic data.</li>
                    <li>Coordination with <strong>local police and disaster response units</strong> during emergencies or criminal investigations.</li>
                    <li>Compliance with <strong>court orders, subpoenas,</strong> and other legal processes.</li>
                    <li>Sharing with <strong>Municipal/City Government</strong> for consolidated LGU reports and inter-barangay coordination.</li>
                </ul>
                <p class="text-sm text-gray-600 mt-2">
                    Data sharing follows strict protocols and is limited to what is necessary and lawful. Residents will be notified when possible, except when prohibited by law or court order.
                </p>
            </section>

            <section>
                <h2 class="text-xl font-bold mt-6 mb-3">X. Changes to This Privacy Policy</h2>
                <p>
                    We reserve the right to update this Privacy Policy to reflect changes in our practices, technology, or legal requirements. Users will be notified of significant changes via email, SMS, in-app notification, or public posting at the Barangay Hall bulletin board. Continued use of the RESIDENTE service after such modifications constitutes acceptance of the updated policy.
                </p>
